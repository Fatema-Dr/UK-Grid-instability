def export_project_files():
    print("Packaging project for Local Dashboard...")

    # Create a folder for export
    os.makedirs("export_pack", exist_ok=True)

    # 1. Save LightGBM Classifier Model
    joblib.dump(global_model, LGBM_MODEL_PATH)

    # 2. Save LightGBM Quantile Models
    joblib.dump(lower_model, LGBM_QUANTILE_LOWER_PATH)
    joblib.dump(upper_model, LGBM_QUANTILE_UPPER_PATH)

    # 3. Save LSTM Model
    # Keras models need a specific format
    # The `model_lstm` is directly available as it's returned by train_lstm_model
    model_lstm.save(LSTM_MODEL_PATH)

    # 4. Save the Scaler (CRITICAL)
    joblib.dump(global_scaler, SCALER_PATH)

    # 5. Save a "Demo Slice" of Data
    # We can't load 2.6 million rows in the dashboard easily.
    # Let's save just August 9th (The Blackout Day) for the demo.

    # Re-create the test slice
    split_date = "2019-08-09 00:00:00"
    df_demo = df_processed.filter(
        (pl.col("timestamp") >= pl.lit(split_date)) &
        (pl.col("timestamp") < pl.lit("2019-08-10 00:00:00"))
    )
    # Save as CSV
    df_demo.write_csv("export_pack/demo_data_aug9.csv")

    print("Files saved to /content/export_pack/")

    # Zip it up for easy download
    shutil.make_archive("/content/project_export", 'zip', "export_pack")
    print("Zipping complete. Download 'project_export.zip' from the Files tab!")

# EXECUTE
export_project_files()