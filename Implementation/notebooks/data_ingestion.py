def explain_predictions(model, X_test, df_test_polars):
    print("Generating Explanations (SHAP Values)...")

    # 1. Get Probabilities instead of just 0/1
    # ----------------------------------------
    # model.predict_proba returns [Prob_Stable, Prob_Unstable]
    # We want the probability of 'Unstable' (Index 1)
    y_prob = model.predict_proba(X_test)[:, 1]

    # Add probability to the test dataframe for inspection
    # We need to reconstruct the dataframe temporarily
    df_results = df_test_polars.select(["timestamp", "grid_frequency", "target_is_unstable"]).to_pandas()
    df_results["alert_probability"] = y_prob

    # 2. Inspect the "Blackout Moment" (Approx 16:52 UTC on Aug 9)
    # -----------------------------------------------------------
    print("\nTop 5 Highest Risk Moments on August 9:")
    print(df_results.sort_values("alert_probability", ascending=False).head(5))

    # 3. SHAP Analysis (Why did the model predict Unstable?)
    # ------------------------------------------------------
    # We use TreeExplainer for LightGBM
    explainer = shap.TreeExplainer(model)

    # Calculate SHAP values for a subset (calculating for all 86k rows is slow)
    # Let's pick the top 100 "highest risk" rows to understand what drives alarms
    high_risk_indices = df_results.sort_values("alert_probability", ascending=False).head(100).index
    X_for_shap = X_test.iloc[high_risk_indices]
    shap_values = explainer.shap_values(X_for_shap)

    # Ensure shap_values[1] is a 2D array (matrix) for summary_plot
    shap_values_for_plot = shap_values[1]
    if isinstance(shap_values_for_plot, list): # Check if it's a list (e.g., from multi-output explainer for binary classification)
        if len(shap_values_for_plot) == 1:
            shap_values_for_plot = shap_values_for_plot[0]
        else:
            # If it's a list of multiple arrays, and we want to plot for class 1, ensure it's still treated correctly
            # This case might not be directly applicable for binary classification where shap_values[1] is typically one array
            # For now, if it's a list with multiple elements, and we're picking index 1, assume it's the correct array.
            # If it leads to errors, further debugging of shap_values structure would be needed.
            shap_values_for_plot = shap_values_for_plot # Keep as is if it's already the desired array for class 1

    if isinstance(shap_values_for_plot, np.ndarray) and shap_values_for_plot.ndim == 1:
        # Assuming it's a flattened array of (num_samples * num_features) or (num_features,)
        # Try to reshape to (num_samples, num_features)
        if shap_values_for_plot.size == X_for_shap.shape[0] * X_for_shap.shape[1]:
            shap_values_for_plot = shap_values_for_plot.reshape(X_for_shap.shape[0], X_for_shap.shape[1])
        elif shap_values_for_plot.size == X_for_shap.shape[1]: # If it's features for a single sample
            shap_values_for_plot = shap_values_for_plot.reshape(1, -1)
        elif shap_values_for_plot.size == X_for_shap.shape[0]: # If it's a single value per sample, but needs to be 2D
            shap_values_for_plot = shap_values_for_plot.reshape(-1, 1)
        else:
            print(f"Warning: shap_values[1] has unexpected 1D shape {shap_values_for_plot.shape} that cannot be easily reshaped to 2D for plotting. Attempting to proceed with current shape, plot might fail or be incorrect.")


    # 4. PLOT: Summary Plot
    # ---------------------
    plt.figure(figsize=(10, 6))
    shap.summary_plot(shap_values_for_plot, X_for_shap, plot_type="bar", show=False)
    plt.title("What Drives the Alert System? (Top Risk Factors)")
    plt.show()

    return df_results

# EXECUTE
